import 'package:flutter/material.dart';

/// Reusable error message widget for the application.
///
/// Displays an error icon, message, and optional retry button.
/// Use this widget when API requests fail or errors occur.
class ErrorMessage extends StatelessWidget {
  /// The error message to display
  final String message;

  /// Optional callback for the retry action
  final VoidCallback? onRetry;

  /// Creates an ErrorMessage widget.
  const ErrorMessage({
    super.key,
    required this.message,
    this.onRetry,
  });

  @override
  Widget build(BuildContext context) {
    return Center(
      child: Padding(
        padding: const EdgeInsets.all(24.0),
        child: Column(
          mainAxisAlignment: MainAxisAlignment.center,
          children: [
            const Icon(
              Icons.error_outline,
              size: 64,
              color: Colors.red,
            ),
            const SizedBox(height: 16),
            Text(
              message,
              style: const TextStyle(
                fontSize: 16,
                color: Colors.grey,
              ),
              textAlign: TextAlign.center,
            ),
            if (onRetry != null) ...[
              const SizedBox(height: 24),
              ElevatedButton.icon(
                onPressed: onRetry,
                icon: const Icon(Icons.refresh),
                label: const Text('Tentar novamente'),
              ),
            ],
          ],
        ),
      ),
    );
  }
}
